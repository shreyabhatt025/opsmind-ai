// db.js
// this file does 2 things:
// 1. connects our app to mongodb atlas
// 2. defines what a "chunk" looks like when saved in the database

// mongoose is like a bridge between our javascript code and mongodb
const mongoose = require('mongoose')

// dotenv loads our .env file so we can use process.env.MONGODB_URI
// if we don't call this, process.env.MONGODB_URI will be undefined
require('dotenv').config()


// schema 

const chunkSchema = new mongoose.Schema({

    // the actual paragraph text from the pdf
    // example: "To process a refund, go to billing module..."
    text: {
        type: String,  
        required: true
    },

    // this is the vector - array of 1536 numbers generated by openai
    // example: [0.023, -0.871, 0.341, 0.009, 0.112, ...]
    // mongodb will store and search through these numbers later
    embedding: {
        type: [Number],
        required: true
    },

    // which pdf file this chunk came from
    // example: "Refund_Policy_SOP.pdf"
    // this is what we show as citation in the final answer
    sourceFile: {
        type: String,
        required: true
    },

    // chunk number - so we know it was the 4th chunk from this file
    // useful for ordering and debugging
    chunkIndex: {
        type: Number,
        required: true
    },

    // chunk date and time automatically stored 
    createdAt: {
        type: Date,
        default: Date.now
    }

})
const Chunk = mongoose.model('Chunk', chunkSchema)
async function connectDB() {
    try {

        
        await mongoose.connect(process.env.MONGODB_URI)

        console.log('mongodb connected successfully!')

    } catch (error) {

        console.log('mongodb connection error:', error.message)
        process.exit(1)
    }
}



// saveChunk - call this for every chunk after embedding it
// it creates a new document in the chunks collection

async function saveChunk(chunkData) {

    // create a new chunk object using our schema
    // chunkData should have: text, embedding, sourceFile, chunkIndex
    const newChunk = new Chunk(chunkData)

    // .save() actually sends it to mongodb atlas
    // await means we wait for it to finish before moving on
    await newChunk.save()

    return newChunk
}


// we export both functions so other files can use them
// server.js will call connectDB() on startup
// server.js will call saveChunk() for every chunk it processes
module.exports = { connectDB, saveChunk }